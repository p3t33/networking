DataProxy::DataProxy(std::string name_of_output_file)
{
    m_file.open(name_of_output_file, std::ofstream::app);
}
                                                                    
/*~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~*/
/*                                                          Destructor / dtor */
/*                                                          ~~~~~~~~~~~~~~~~~ */
DataProxy::~DataProxy()
{
    m_file.close();
}

/*============================================================================*/
/*                               ~~~~~~~~~~~~~~~~                             */
/*                               member functions                             */
/*                               ~~~~~~~~~~~~~~~~                             */
/*                                                              write_to_file */
/*                                                              ~~~~~~~~~~~~~ */
void DataProxy::write_to_file(const std::string& string)
{
    m_file << std::flush;
    m_file << string.c_str() << " ";
}

